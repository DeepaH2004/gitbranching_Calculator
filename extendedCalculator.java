public class extendedCalculator {

    float triangle() {
        float operand_1 = 10;
        float operand_2 = 5;
        return (1 / 2 * operand_1 * operand_2);
    }

    float modulus() {
        float operand_1 = 10;
        float operand_2 = 5;
        return (operand_1 % operand_2);

    }
}
